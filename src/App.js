const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "53751975";

export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");

  const query = "fkvol";

  useEffect(function () {
    async function fetchMovies() {
      try {
        /* Set a Loading State */
        setIsLoading(true);

        /* Fetch an API */
        const res = await fetch(
          `https://www.omdbapi.com/?apikey=${KEY}&s=${query}`
        );

        /* If failed to Fetch, throw an Error */
        if (!res.ok) throw new Error("Failed to fetch, Please Try Again");

        /* Set result into data, when Fetched */
        const data = await res.json();
        console.log(data);

        /* If Results not found, throw an Error */
        if (data.Response === "False") throw new Error("Movie Not Found!");

        /* Input search results into setMovies */
        setMovies(data.Search);
      } catch (err) {
        /* Set the error message into a State */
        setError(err.message);
      } finally {
        setIsLoading(false);
      }
    }

    fetchMovies();
  }, []);

  return (
    <>
      <Navbar>
        <Search />
        <NumResults movies={movies} />
      </Navbar>

      <Main>
        <Box>
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />} */}
          {isLoading && <Loader />}
          {!isLoading && !error && <MovieList movies={movies} />}

          {error && <ErrorMessage message={error} />}
        </Box>

        <Box>
          <WatchedSummary watched={watched} />
          <WatchedMovieList watched={watched} />
        </Box>
      </Main>
    </>
  );
}

